                    <h4 align="left"><b>1.新加坡南洋理工大學教授張善勇蒞校演講</b></h4>
                    <p>新加坡南洋理工大學教授張善勇博士到亞洲大學演講，並與亞大半導體實驗室，進行學術交流、討論合作事宜。</p>
                    <img src="https://i.imgur.com/BxznI7x.jpg" align="right" style="height: auto;width: 15em">
                    <p>新加坡南洋理工大學教授張善勇博士(Dr. Sam Zhang)，10月2日下午應邀到亞洲大學（Asia University, Taiwan）演講，他說，染料敏化太陽能電池，是目前所有太陽能電池技術中製作成本最低的技術，可惜未能量產，主要是其轉換效率較低的緣故；如何應用奈米科技，進行電池電極製作與優化，以獲得更高的轉換效率，是他目前研究重點。</p>
                    <img src="https://i.imgur.com/CRO91qf.jpg" align="left" style="height: auto;width: 15em">
                    <p>張善勇教授是以「染料敏化太陽能電池的現況與未來發展」為題，向亞洲大學資訊工程學系師生、半導體產研專班國際生演講；張教授說，他於1991年剛到新加坡任職時，即發現當地有充足日光，值得研發太陽能技術，但是未獲學校支持，錯過太陽能研究的先機，現在回想起來，如果當時堅持下去，到現在對人類再生能源與太陽能技術的發展，一定有更大的貢獻。</p>

                    <p>張教授除了以個人的研究經歷，讓學生瞭解現今再生能源、太陽能電池及奈米技術發展的脈動；他還勉勵在場學生，在確認正確研究方向後，要持續努力並堅持到底，一門深入，最後的研究成果，必定能夠對國家社會，甚至對全人類有所貢獻。</p>
                    <p>張教授此次來訪，也參觀亞洲大學半導體實驗室，與實驗室負責人許健教授學術交流；許健教授指出，亞大半導體實驗室是國內大學中很獨特的學術團隊，研發「矽前製程」（pre-Silicon)，先後獲得華邦電子、世界先進、新唐科技、台積電等電子大廠資助委託研發及產學合作，他也已完成相關實驗室的佈建工程，希望該實驗室能成為台灣及亞洲的奈米技術領先的實驗室；將來有機會將與張教授合作開發奈米太陽能電池技術，獲得更高轉換效率的太陽能電池。</p>
                    <p>亞大資工系主任陳永欽介紹說，張善勇教授是國際知名學者，是美國威斯康辛大學博士，目前是新加坡南洋理工大學機械及航空工程學院終身教授，薄膜學會創始人和現任會長；現任美國材料學會《材料研究雜誌》主編、《奈米科技通訊》雜誌總編、中國中科院固體物理研究所名譽教授、浙江大學、哈爾濱工業大學客座教授；2007年獲頒英國材料學會會士 (等同台灣中研院)，現任先進金屬材料塗鍍國家工程實驗室首席科學家，具有豐富學術研究經歷。</p>
